	    margin: 5px .1em;
	    padding: 6px 1em;
	    width: 6em;
	    color: #fff;
	    text-align: center;
	    text-transform: uppercase;
	    font-size: 12px;
	    font-size: 1.2rem;
	}</code>
	</pre>
